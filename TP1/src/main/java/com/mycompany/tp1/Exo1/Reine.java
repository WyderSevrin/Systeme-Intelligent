public class Reine {
    int x =0;
    int y =0;

    public Reine(int x,int y) {
        this.x=x;
        this.y=y;
    }

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
